    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
         int row = table2.getSelectedRow();
        if(row != -1){
           Object[] obboi = {"Yes","NO"};
           int i = JOptionPane.showOptionDialog(this, "Delete Order from the order table", "Delete",JOptionPane.OK_OPTION,JOptionPane.QUESTION_MESSAGE,null,obboi,obboi[1]);
            
           if(i == 0){
               
               OrderMenu o = otm.getOrderMenu(row);
               try {
                  or.Delete(o); 
                   totalP();
                  
               } catch (Exception e) {
                   JOptionPane.showMessageDialog(this, e.getMessage());
               }
            
            loadTable2();
           }else{
            
            loadTable2();
            }
        
        
        }else{
           JOptionPane.showMessageDialog(this, "Select the order you want to delete");
        }
         
    }//GEN-LAST:event_DeleteActionPerformed

