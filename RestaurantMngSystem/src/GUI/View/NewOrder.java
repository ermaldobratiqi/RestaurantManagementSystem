    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed
           int row = table1.getSelectedRow(); 
        if(row == -1){ 
           JOptionPane.showMessageDialog(this, "Select the item you want to order");
        }else {
         try { 
            Date date = new Date();
            DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");  
            String strDate = dateFormat.format(date);
            OrderMenu o = new OrderMenu();
            PerdoruesiRepository pr = new PerdoruesiRepository();       
            Ushqimet u = utm.getUshqimet(row);
            LoginForm l = new LoginForm("d");
            Perdoruesi p = pr.loginByUsernameAndPassword(l.getUser(), l.getPass()); 
            o.setEmriUshqimitId(u);
            o.setPerdoruesiId(p);
            o.setDataEPorosis(strDate);
            or.Create(o);
            totalP();
            BigDecimal t = u.getCmimi();
            loadTable2();
         } catch (CrudFormException ex) {
             Logger.getLogger(NewOrder.class.getName()).log(Level.SEVERE, null, ex);
         }
        
        
        
        
        
        }
        
    }//GEN-LAST:event_AddActionPerformed

